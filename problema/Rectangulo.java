package problema;

public class Rectangulo {
    
    private int base;
    private int altura;
    public int id;

    public Rectangulo(int base, int altura, int id) {
        this.base = base;
        this.altura = altura;
        this.id = id;
    }

    public int getBase() {
        return base;
    }

    public int getAltura() {
        return altura;
    }

    public int getId() {
        return id;
    }
    
}
